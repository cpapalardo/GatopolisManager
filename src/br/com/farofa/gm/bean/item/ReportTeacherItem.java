	private int dashboardOpenedTotal;
	private int dashboardDurationTotal;
	private int durationAbaObsTotal;
	private int durationAbaProdTotal;
	private int durationAbaFreqTotal;
	private int durationTransitionsTotal;
	private int durationGroupingTotal;

